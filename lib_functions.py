def region_of_interest(img, vertices):
    """
    Applies an image mask.
    
    Only keeps the region of the image defined by the polygon
    formed from `vertices`. The rest of the image is set to black.
    `vertices` should be a numpy array of integer points.
    """
    #defining a blank mask to start with
    mask = np.zeros_like(img)   
    
    #defining a 3 channel or 1 channel color to fill the mask with depending on the input image
    if len(img.shape) > 2:
        channel_count = img.shape[2]  # i.e. 3 or 4 depending on your image
        ignore_mask_color = (255,) * channel_count
    else:
        ignore_mask_color = 255
        
    #filling pixels inside the polygon defined by "vertices" with the fill color    
    cv2.fillPoly(mask, vertices, ignore_mask_color)
    
    #returning the image only where mask pixels are nonzero
    masked_image = cv2.bitwise_and(img, mask)
    return masked_image


def draw_lines(img, lines, vertices_region,color=[255, 0, 0], thickness=5):
    """
    NOTE: this is the function you might want to use as a starting point once you want to 
    average/extrapolate the line segments you detect to map out the full
    extent of the lane (going from the result shown in raw-lines-example.mp4
    to that shown in P1_example.mp4).  
    Think about things like separating line segments by their 
    slope ((y2-y1)/(x2-x1)) to decide which segments are part of the left
    line vs. the right line.  Then, you can average the position of each of 
    the lines and extrapolate to the top and bottom of the lane.
    
    This function draws `lines` with `color` and `thickness`.    
    Lines are drawn on the image inplace (mutates the image).
    If you want to make the lines semi-transparent, think about combining
    this function with the weighted_img() function below
    """
    buttom_y=int(vertices_region[0,0][1])
    up_y=int(vertices_region[0,1][1])+20
    color_avg=[255, 215, 0]
    avg_thkns=3
    left_lane=np.empty((0,4), int)
    right_lane=np.empty((0,4), int)
    for line in lines:
        for x1,y1,x2,y2 in line:
            slope_line=(y2-y1)/(x2-x1)
            #print(slope_line)
            #ignore horizontal lines and cluster left and right lanes
            if slope_line > 0.4:
                left_lane=np.append(left_lane,np.array(line),axis=0)
                cv2.line(img, (x1, y1), (x2, y2), color, thickness)
            elif slope_line <-0.4:
                right_lane=np.append(right_lane,np.array(line),axis=0)
                cv2.line(img, (x1, y1), (x2, y2), color, thickness)
    avg_line_left=left_lane.mean(axis=0).astype(int)
    avg_line_right=right_lane.mean(axis=0).astype(int)
    avg_slope_left=(avg_line_left[3]-avg_line_left[1])/(avg_line_left[2]-avg_line_left[0])
    avg_slope_right=(avg_line_right[3]-avg_line_right[1])/(avg_line_right[2]-avg_line_right[0])
    b_left=avg_line_left[3] - avg_slope_left*avg_line_left[2]
    b_right=avg_line_right[3] - avg_slope_right*avg_line_right[2]
    buttom_x_left=int((buttom_y-b_left)/avg_slope_left)
    buttom_x_right=int((buttom_y-b_right)/avg_slope_right)
    up_x_left=int((up_y-b_left)/avg_slope_left)
    up_x_right=int((up_y-b_right)/avg_slope_right)
    cv2.line(img, (buttom_x_left, buttom_y), (up_x_left, up_y), color_avg, avg_thkns)
    cv2.line(img, (buttom_x_right, buttom_y), (up_x_right, up_y), color_avg, avg_thkns)
